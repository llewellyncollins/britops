export function getAllSpecialties(procedures: ProcedureType[]): string[] {
  return [...new Set(procedures.map((p) => p.specialty))].sort();
}

export function getCategoriesForSpecialty(
  procedures: ProcedureType[],
  specialty: string,
): string[] {
  return [
    ...new Set(
      procedures
        .filter((p) => p.specialty === specialty)
        .map((p) => p.category),
    ),
  ];
}
